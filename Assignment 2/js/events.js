function showOverall() {
	var overallSummary = document.getElementById('overall');
	var chemistrySummary = document.getElementById('chemistry');
	var economicsSummary = document.getElementById('economics');
	var literatureSummary = document.getElementById('literature');
	var medicineSummary = document.getElementById('medicine');
	var peaceSummary = document.getElementById('peace');
	var physicsSummary = document.getElementById('physics');

	overallSummary.style.display = "block";
	chemistrySummary.style.display = "none";
	economicsSummary.style.display = "none";
	literatureSummary.style.display = "none";
	medicineSummary.style.display = "none";
	peaceSummary.style.display = "none";
	physicsSummary.style.display = "none";
}

